          <h5 class="text-center">Vorwort</h5>
          <br/><br/>
          <p>
           Die Prüfung Zertifikat B1 wurde in trinationaler Zusammenarbeit gemeinsam vom Goethe Institut/Deutschland,
dem ÖSD/Österreich und der Universität Freiburg/Schweiz neu entwickelt.
          </p>
           <p>
           Sie wird weltweit nach einheitlichen Kriterien durchgeführt und ausgewertet.
          </p>
           <p>
           	Die Prüfung Zertifikat B1 richtet sich an Jugendliche und Erwachsene. <br>
Für das Zertifikat B1 für Jugendliche wird ein Alter ab 12 Jahren empfohlen und für das Zertifikat B1 für Erwachsene
ein Alter ab 16 Jahren.
          </p>
           <p>
           Die Deutschprüfung dokumentiert die dritte Stufe – B1 – der im <i>Gemeinsamen europäischen Referenzrahmen für
Sprachen </i> beschriebenen sechsstufigen Kompetenzskala. Die Stufe B bezeichnet die Fähigkeit zur selbstständigen
Sprachverwendung. Das Niveau wurde durch Experten aus ganz Europa begutachtet und bestätigt.
          </p>
           <p>
           Mit erfolgreichem Ablegen der Prüfung haben Teilnehmende nachgewiesen, dass sie sich in allen wichtigen
Alltagssituationen sprachlich zurechtfinden, wenn die Gesprächspartner klare Standardsprache verwenden.
          </p>
           <p>
           Sie können:
          </p>
          
           		<ul>
						<li>die Hauptinformationen verstehen, wenn klare Standardsprache verwendet wird und wenn es um
vertraute Dinge aus Arbeit, Schule, Freizeit usw. geht.</li>   
						<li>die meisten Situationen bewältigen, denen man auf Reisen in deutschsprachigen Ländern begegnet.</li>
						<li>sich einfach und zusammenhängend über vertraute Themen und persönliche Interessensgebiete äußern.</li>
						<li>über Erfahrungen und Ereignisse berichten und Träume, Hoffnungen und Ziele beschreiben sowie kurze
Begründungen oder Erklärungen geben.</li>        		
           		</ul>
        
           <p>
           Geprüft werden die vier Fertigkeiten Lesen, Hören, Schreiben und Sprechen. Diese können in einzelnen Modulen
oder wie gewohnt als Gesamtprüfung abgelegt werden. (HIER MÜSSTE BITTE NOCH FORMULIERT WERDEN, DASS IM ONLINE-TEST DIE LEISTUNG FÜR DIE FERTIGKEITEN LESEN UDN HÖREN AUSGEWERTET WERDEN KANN, DASS ES FÜR SCHREIBEN UND SPRECHEN ABER NUR MUSTERBEISPIELE GIBT.)  
          </p>
          	<p>
          		In der Prüfung lassen sich maximal 100 Punkte pro Modul erreichen. Die Bestehensgrenze liegt bei 60 Punkten, also 60 %.
          	</p>
				<p>
					Der vorliegende Modellsatz entspricht in Aufgabentypen, Itemzahl, Zeitvorgaben den Originalaufgaben der
Prüfung Zertifikat B1. Sie können damit eine Prüfungssituation simulieren, wenn Sie die Aufgaben wie unter echten
Prüfungsbedingungen bearbeiten.
				</p>
				<p>
					Wir wünschen den Teilnehmenden viel Erfolg bei der Vorbereitung!				
